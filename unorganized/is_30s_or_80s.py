#is thirties or eighties

input_number = int(input('Give me a number\n:'))

if input_number <= 39 and input_number >= 30:
    print("That number is in the thirties.")

elif input_number <= 89 and input_number >= 80:
    print("That number is in the eighties.")

else:
    print("The number is not in the thirties or the eighties")
